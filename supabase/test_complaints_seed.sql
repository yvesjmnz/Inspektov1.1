declare
  v_creator uuid;
begin
  select id
  into v_creator
  from public.profiles
  where role in ('director', 'head_inspector', 'head inspector')
  order by created_at nulls last
  limit 1;

  if v_creator is not null then
    insert into public.mission_orders (
      id,
      complaint_id,
      created_by,
      status,
      title,
      content,
      created_at,
      updated_at,
